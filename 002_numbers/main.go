package main

import "fmt"

func main() {
	fmt.Println(5 + 5)
	fmt.Println(5 - 5)
	fmt.Println(3 * 5)
	fmt.Println(5 / 5)
	fmt.Println(15 % 4)
	// Remainder
	fmt.Println(5 % 5)
	fmt.Println(5 % 3)
	fmt.Println(7 % 3)
	fmt.Println(7 % 3)
	fmt.Println(7 % 2)
	fmt.Println(18 % 5)
	fmt.Println(18 % 6)
}

// When we have to pass a word or a sentence we have to use double quotes
// When we have to pass a number we have to use no quotes
// Goalng as the following operators for arithmetic operations
// +, -, *, /, %, ++, --
